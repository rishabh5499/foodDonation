package com.example.fooddonation;

public class details {
    String platesno;
    String locality;
    String volunteerNeed;
    String picked;

    public String getPicked() {
        return picked;
    }

    public void setPicked(String picked) {
        this.picked = picked;
    }

    public String getVolunteerNeed() {
        return volunteerNeed;
    }

    public void setVolunteerNeed(String volunteerNeed) {
        this.volunteerNeed = volunteerNeed;
    }

    public String getPlatesno() {
        return platesno;
    }

    public void setPlatesno(String platesno) {
        this.platesno = platesno;
    }

    public String getLocality() {
        return locality;
    }

    public void setLocality(String locality) {
        this.locality = locality;
    }
}
